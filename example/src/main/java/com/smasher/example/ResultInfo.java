/**
 * @author Smasher
 * on 2020/4/17 0017
 */
public class ResultInfo {
    private int state;
    private long size;
    private long total;


    public int getState() {
        return state;
    }

    public void setState(int state) {
        this.state = state;
    }

    public long getSize() {
        return size;
    }

    public void setSize(long size) {
        this.size = size;
    }

    public long getTotal() {
        return total;
    }

    public void setTotal(long total) {
        this.total = total;
    }
}
